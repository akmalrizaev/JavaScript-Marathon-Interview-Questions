// Template Literal gives a way to display the expression inside the backtick (`) itself

let a = 90;
let str = `Value of a is ${a}`;
console.log(str); // Value of a is 90

//  Template String helps us to store the format as it is then escape sequence is not required
// for special characters/spaces/new lines
let str2 = `This is
     second line and
	             this is the third one`;
console.log(str2);
